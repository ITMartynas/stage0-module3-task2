package school.mjc.stage0.module3.task2;

public class CopiedNames {
    public static void main(String[] args) {
        int variableOne = 0;
        int variableTwo = 2000;
        int valueOne = 1000;
        int variableThree = 4000;
        int valueTwo = 3000;
        int variablesOne = 5000;
        int variablesTwo = 7000;
        int valueThree = 90000;

        System.out.println("variableOne: " + variableOne);
        System.out.println("variableTwo: " + variableTwo);
        System.out.println("valueOne: " + valueOne);
        System.out.println("variableThree: " + variableThree);
        System.out.println("valueTwo: " + valueTwo);
        System.out.println("variablesOne: " + variablesOne);
        System.out.println("variablesTwo: " + variablesTwo);
        System.out.println("valueThree: " + valueThree);
    }
}
